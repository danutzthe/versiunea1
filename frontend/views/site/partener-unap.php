            Partener<br><strong>Universitatea Nationala de Aparare "Carol I"</strong>
        </h1>

        <a href="http://www.unap.ro/index.php/ro/" target="_blank"><img src="<?php echo Yii::$app->request->baseUrl . "/assets/img/unap.jpg" ?>" style="float: right; margin-top: 20px;"></a>

        <p style="margin: 145px 0 10px 0; text-align: justify; text-indent: 30px; padding-left: 0; padding-right: 0;">
            Universitatea a fost fondată în anul 1889, de către generalul Ștefan Fălcoianu, fiind a
            şasea scoală de stat major din Europa, alături de cele din Berlin, Viena, Paris, Torino
            şi Bruxelles, având iniţial numele de "Şcoala Superioară de Război".
        </p>
        <p style="margin: 10px 0; text-align: justify; text-indent: 30px; padding-left: 0; padding-right: 0;">
            Influenţele franceză, germană şi sovietică şi-au făcut simţite prezenţa în procesul de
            pregătire în cadrul acestei instituţii. "LABOR IMPROBUS OMNIA VINCIT" - Munca stăruitoare
            învinge întotdeauna! Acesta este motto-ul care a stat la baza educării şi pregătirii a unui
            numar foarte mare de ofiţeri din toate categoriile de forţe armate, de la înfiinţare şi
            până în prezent.
        </p>
        <p style="margin: 10px 0; text-align: justify; text-indent: 30px; padding-left: 0; padding-right: 0;">
            Investiţia în educaţie are efecte pe termen lung iar principiul e valabil şi pentru domeniul
            securităţii şi apărării naţionale.
        </p>
        <p style="margin: 10px 0; text-align: justify; text-indent: 30px; padding-left: 0; padding-right: 0;">
            Învăţământul în Universitatea Naţională de Apărare "Carol I" asigură o pregătire completă şi
            complexă menită să consolideze instrumentarul ştiinţific şi metodologia necesară utilizării
            superioare a principiilor şi normelor acţiunii şi conducerii militare de nivel tactic
            interarme, operativ şi strategic în timp de pace, criză şi război.
        </p>

        <p style="margin: 10px 0; text-align: justify; text-indent: 30px; padding-left: 0; padding-right: 0;">
            În cadrul proiectului se regăsesc cele două direcţii principale spre care este orientată
            <b>Universitatea Naţională de Apărare "Carol I" (UNAp)</b>:
        </p><ol style="margin-left: 10px;">
            <li style="text-align: left;">formarea de lideri şi experţi (militare şi civile) în domeniul apărării şi securităţii naţionale.</li>
            <li style="text-align: left;">elaborarea de studii ştiintifice solicitate de structurile cu atribuţii în domeniul
                apărării şi securităţii naţionale (Preşedinţie, Guvern, Consiliul Suprem de
                Apărare a Ţării, Parlamentul European). Universitatea a acumulat o experienţă
                importantă în proiectele de cercetare şi dezvoltare tehnologică atât ca şi
                coordonator cât şi ca partener în proiecte importante în domeniul securităţii şi
                apărării naţionale. Dintre proiectele de cercetare anterioare şi în derulare, care
                au legătură cu obiectivele acestui proiect amintim:
                <ul>
                    <li>
                        PROSTRACT, Programul Naţional de Cercetare 2007-2013 "Cercetare interdisciplinară pe
                        proiectarea strategiilor economico-financiare de acţiune în timpul evenimentelor
                        de risc extrem. Riscuri naturale şi accidente tehnologice", 2008-2010
                    </li>
                    <li>SMEFIP, Programul Naţional de Cercetare 2007-2013 "Sisteme de management
                        economic şi financiar integrat pentru protejarea infrastructurii critice
                        şi a personalului împotriva terorismului", 2008-2010</li>
                    <li>Programul Cercetare de Excelenta, "Securitatea sistemelor şi acţiunilor
                        militare şi civil-militare în timpul de gestionării crizelor şi conflictelor
                        militare", 2006-2008</li>
                    <li>CPeL-SNMC, Centru pilot eLearning pentru dezvoltarea şi distribuirea de
                        conţinut digital în domeniul securităţii naţionale şi managementului
                        crizelor, 2005-2007</li>
                    <li>MEDSCEN, Programul Naţional de Cercetare 2007-2013, "Cercetari aprofundate
                        pentru crearea unui sistem educational pilot in spatiul virtual pentru
                        simularea scenariilor privind dezastrele naturale si a modului de acţiune
                        al cetăţenilor în situaţii de criză", 2007-2010</li>
                    <li>Gala - Games and Learning Alliance - Reţea de Excelenta, 2010 - 2014,
                        FP7-ICT-2009 - 5 ICT-2009.4.2.</li>
                </ul>
            </li>
