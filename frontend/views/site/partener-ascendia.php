        <p style="margin: 145px 0 10px 0; text-align: justify; text-indent: 30px; padding-left: 0; padding-right: 0;">
            <b>Ascendia Design (Ascendia)</b> este un IMM înfiinţat în 2007, a cărui principal domeniu
            de expertiză este dezvoltarea de soluţii de eLearning şi produse multimedia. In aproximativ
            cinci ani de existenţă, compania a dezvoltat mai mult de 7000 de obiecte de eLearning
            interactive pentru vârstele cuprinse între 3 şi 50+ de ani, precum şi mai multe aplicaţii
            complexe internet pentru diverşi clienti din întreaga lume.
        </p>
        <p style="margin: 10px 0; text-align: justify; text-indent: 30px; padding-left: 0; padding-right: 0;">
            Arie de expertiză:
        </p><ul style="margin-left: 10px; text-align: left;">
            <li>cursuri eLearning, în mod special cursuri conforme cu standardele SCORM 2004 şi AIC;</li>
            <li>aplicaţii interactive (modelare 3D, simulări virtuale, modele ştiinţifice, laboratoare virtuale);</li>
            <li>motoare de jocuri multiplayer, sincron sau de strategie pe ture;</li>
            <li>prezentări multimedia (video, sunet, interactivitate, simulări 3D);</li>
            <li>aplicaţii şi portaluri web;</li>
            <li>aplicaţii pentru device-uri mobile (smartphone &amp; tablete).</li>
        </ul>
        <p></p>
        <p style="margin: 10px 0; text-align: justify; text-indent: 30px; padding-left: 0; padding-right: 0;">
            Membrii echipei au experienţă solidă în dezvoltarea de software, cu un accent puternic
            pentru aplicaţii eLearning multiuser şi multirol – atât pe platforme eLearning cât şi
            independent. În 2011 companiei i-a fost acordat statutul de companie cu facilităţi de
            cercetare şi dezvoltare (R &amp; D), ca o recunoaştere pentru interesul şi realizările din
            proiecte anterioare legate de cercetare şi dezvoltare. (ANCS decizia nr. 9508/25.01.2011).
            Ascendia a dezvoltat soluţii de eLearning, mLearning şi jocuri educative pentru diferite
            categorii de vârstă, pentru sectorul atât academic şi cel privat. Aplicaţiile Ascendia pot
            fi regăsite ca aplicaţii de sine stătătoare, cum ar fi EduTeca suite- 8 CD-uri, disponibile
            pe www.eduteca.ro, aplicaţii pentru preşcolari pot fi descărcate pe iStore Apple, de
            exemplu Innerfox şi Patterngod, sau jocuri serioase pentru adulţi on-line, cum ar fi
            "Afacereza online" sau "Politiq "- un jocuri multiplayer disponibile la <a href="http://www.politiq.ro">www.politiq.ro</a>.
            Ascendia Design a dezvoltat de asemenea proiecte de cercetare şi dezvoltare, atât proprii
            cât şi ca parte externalizată a unui proiect. Rezultatul acestora a fost un set de
            tehnologii şi biblioteci software. Un exemplu este MathML Render –o componentă software
            care permite aplicaţiilor dezvoltate în Adobe Flash pentru a afişa formulele Academic Math
            în mediile online. Rezultatele activităţilor de cercetare şi dezvoltare au fost
            transferate în proiectele curente ale companiei sau au fost autorizate de clienţii şi
            parteneri, în scopul de a fi încorporate în proiectele lor. Expertiza companiei a fost
            recunoscută şi prin desemnarea în calitate de reprezentant al României la World Summit
            Award, ediţia 2011, cu o aplicaţie de eLearning axată pe Astronomie.
