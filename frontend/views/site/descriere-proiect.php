    <h1 class="col-xs-6 col-sm-6 col-md-6 col-lg-6" style="float: left; padding-left: 0; padding-right: 0; text-align: left;"><strong>Descrierea Proiectului</strong></h1>
    <div class="row" style="margin: 10px 0;">
        <p style="text-align: justify;"> <br> <br> <br> <br> <br>
            Motivaţia proiectului MEDGAME provine din recunoaşterea potenţialului pe care
            Serious Games îl oferă  educaţiei şi instruirii şi din necesitatea de a aborda provocările referitoare la
            comportamentul cetăţenilor înainte, în timpul şi după situaţiile de urgenţă. Scopul acestui proiect este de a educa
            populaţia într-un mod inovator şi captivant, pentru a dobândi o atitudine proactivă, bazată pe cunoştinţe şi competenţe
            adecvate şi a unui comportament corect în situaţii de urgenţă cauzate de catastrofe naturale, cum ar fi inundaţiile,
            construind fundamente ştiinţifice solide şi creând un Centru Virtual pentru educarea şi instruirea cetăţenilor
            în situaţii de dezastre naturale prin intermediul Serious Games şi un Framework (cadru) specializat în
            dezvoltarea Serious Games având la bază scenarii bazate pe aceste catastrofe. </p>
        <p style="text-align: justify;">Proiectul foloseşte o concepţie care are ca scop construirea unei documentaţii de bază pentru
            crearea unui Framework care să îmbine motorul şi mecanismele jocului, distracţia, provocarea, dar şi elemente de pedagogie,
            psihologie şi neuroştiinţe, pentru a realiza în final un joc educativ, ca element util în domeniul educaţiei cetăţenilor
            pentru a face faţă situaţiilor de urgenţă. </p>
        <p style="text-align: justify;">În afară de noutatea aspectului tehnologic, acest proiect îşi propune o nouă dimensiune socială
            a jocurilor. Proiectul are ca scop integrarea activităţilor şi resurselor sale într-o perspectivă pe termen lung şi
            oferă capabilităţi pentru a sprijini organizaţiile publice în pregătirea populaţiei şi a comunităţilor pentru a face
            faţă situaţiilor de urgenţă. </p>
        <p style="text-align: justify;">Proiectul va fi dezvoltat pe trei axe majore:
        </p><ol type="1">
            <li>
                <p style="text-align: justify;"> <b>Social: </b> contribuţia la dezvoltarea unui comportament civic activ,
                    implicat(la nivel individ sau comunitate) în cazul situaţiilor de urgenţă.
                </p></li>
            <li>
                <p style="text-align: justify;"><b>Educaţional şi de instruire: </b> conturarea unui mod non-formal de
                    învăţare a adulţilor şi de creare de abilităţi pentru grupuri eterogene de cursanţi, într-un cadru
                    educaţional şi de formare profesională, care să asigure dezvoltarea cunoştinţelor şi a abilităţilor
                    pentru cetăţeni cu scopul de a le schimba comportamentul înainte, în timpul şi după situaţiile de
                    criză/urgenţă.
                </p></li>
            <li>
                <p style="text-align: justify;"> <b>Tehnologic:</b> să întărească capacităţile existente şi infrastructura
                    e-learning realizate într-un proiect anterior MEDSCEN, prin adăugarea funcţionalităţilor Serious Games,
                    atât în ceea ce priveşte mecanismele generice de joc cât şi realizarea unui joc complet.
                </p></li>
        </ol>
        <p></p>
        <p style="text-align: justify;">Proiectul urmăreşte mai multe obiective, dar eforturile principale ale consorţiului se canalizează
            către următoarele direcţii în care se aşteaptă rezultate deosebite: </p>
        <ol type="1">
            <li>
                <p style="text-align: justify;">Să construiască o cultură de securitate şi să dezvolte abilităţi de bază,
                    dar vitale de răspuns la situaţii de criză, în special în cazurile de inundații, prin crearea unui cadru
                    educaţional având un conţinut adecvat şi atractiv;</p></li>
            <li>
                <p style="text-align: justify;">Să ofere servicii  actualizate pentru ridicarea calităţii instruirii
                    pentru situaţiile de urgenţă;
                </p></li>
            <li>
                <p style="text-align: justify;">să dezvolte Serious Games Framework care să servească drept cadru de bază
                    pentru evoluţiile ulterioare ale Serious Games, asigurând astfel, reducerea drastică a costurilor şi
                    stabilirea premiselor pentru dezvoltarea, eficienţa şi durabilitatea Centrului Virtual;
                </p></li>
            <li>
                <p style="text-align: justify;">Să realizeze un joc desfăşurat pe mai multe niveluri, care să fie utilizate
                    atât în modul non-formal (autodidact) cât şi în educaţia formală (şcoli primare, licee), în cadrul orelor
                    de educaţie civică;
                </p></li>
            <li>
                <p style="text-align: justify;">Să creeze un centru de informare-documentare pentru răspunsuri la
                    situaţii de urgenţă care să sprijine autorităţile în asigurarea unor acţiuni eficiente pentru astfel de
                    cazuri;
                </p></li>
            <li>
                <p style="text-align: justify;">Să definească un set de valori pentru eficientizarea instruirii on-line a
                    cetăţenilor prin intermediul Serious Games şi a unui conţinut adecvat-personalizat;
                </p></li>
            <li>
                <p style="text-align: justify;">Să construiască o metodologie pentru a dezvolta scenarii, astfel încât
                    acestea să poată fi folosite atât în cadrul unor Serious Games cât şi ca un mijloc de a organiza sesiuni de
                    instruire.
                </p></li>
        </ol>
    </div>
</div>

